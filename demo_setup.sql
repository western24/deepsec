------------------------------------------------------------------------------
-- 3. PACKAGE / END USER CONTEXT
------------------------------------------------------------------------------

DROP DATA GRANT demo.hospital_ctx_read_grant;
DROP END USER CONTEXT demo.hospital_ctx;
DROP PACKAGE demo.ctx_pkg;

CREATE OR REPLACE PACKAGE demo.ctx_pkg AS
  PROCEDURE init_user_context;
END;
/

CREATE OR REPLACE PACKAGE BODY demo.ctx_pkg AS
  PROCEDURE init_user_context IS
    sql_stmt VARCHAR2(4000);
  BEGIN
    sql_stmt := '
      UPDATE END_USER_CONTEXT t
      SET t.CONTEXT.healthcare_worker_id = (
        SELECT hw.worker_id
        FROM demo.healthcare_workers hw
        WHERE UPPER(hw.username) = UPPER(ORA_END_USER_CONTEXT.username)
      )
      WHERE owner = ''DEMO''
      AND name  = ''HOSPITAL_CTX''
    ';
    EXECUTE IMMEDIATE sql_stmt;
  END;
END;
/

CREATE OR REPLACE END USER CONTEXT demo.hospital_ctx USING JSON SCHEMA '{
  "type":"object",
  "properties":{
    "healthcare_worker_id":{
      "type":"integer",
      "o:onFirstRead":"DEMO.ctx_pkg.init_user_context"
    }
  }
}';


