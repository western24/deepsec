DROP TABLE demo.healthcare_workers;

CREATE TABLE demo.healthcare_workers (
  worker_id    NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  username     VARCHAR2(50) NOT NULL,
  firstname    VARCHAR2(100) NOT NULL,
  lastname     VARCHAR2(100) NOT NULL,
  worker_name  VARCHAR2(100) NOT NULL,
  worker_role  VARCHAR2(20) NOT NULL,
  department   VARCHAR2(100) NOT NULL,
  email        VARCHAR2(200),
  created_at   TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE TABLE demo.patients (
  patient_id           NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  healthcare_worker_id NUMBER NOT NULL,
  patient_name         VARCHAR2(100) NOT NULL,
  sex                  CHAR(1),
  birth_date           DATE NOT NULL,
  phone                VARCHAR2(30),
  prefecture           VARCHAR2(50),
  created_at           TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE TABLE demo.exam_results (
  exam_id         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  patient_id      NUMBER NOT NULL,
  exam_date       DATE NOT NULL,
  height_cm       NUMBER(5,1) NOT NULL,
  weight_kg       NUMBER(5,1) NOT NULL,
  systolic_bp     NUMBER(4) NOT NULL,
  diastolic_bp    NUMBER(4) NOT NULL,
  hba1c           NUMBER(4,2) NOT NULL,
  ldl             NUMBER(5,1) NOT NULL,
  triglyceride    NUMBER(6,1) NOT NULL,
  fasting_glucose NUMBER(5,1) NOT NULL,
  smoking_status  VARCHAR2(20),
  notes           VARCHAR2(4000),
  bmi             NUMBER(4,1)
                    GENERATED ALWAYS AS
                    (ROUND(weight_kg / POWER(height_cm / 100, 2), 1))
                    VIRTUAL
);


